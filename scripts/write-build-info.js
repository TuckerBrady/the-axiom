#!/usr/bin/env node
/* eslint-disable */
const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..');
const OUTPUT_PATH = path.join(REPO_ROOT, 'src', 'buildInfo.ts');

function safeGit(cmd, fallback) {
  try {
    return execSync(`git ${cmd}`, { cwd: REPO_ROOT, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim();
  } catch {
    return fallback;
  }
}

const pkg = JSON.parse(fs.readFileSync(path.join(REPO_ROOT, 'package.json'), 'utf8'));
const version = pkg.version || '0.0.0';
const sha = safeGit('rev-parse --short HEAD', 'unknown');
const buildNumber = safeGit('rev-list --count HEAD', '0');
const branch = safeGit('rev-parse --abbrev-ref HEAD', 'unknown');
const dirty = safeGit('status --porcelain', '').length > 0;
const builtAt = new Date().toISOString();

const contents = `// AUTO-GENERATED by scripts/write-build-info.js. Do not edit by hand.
// Regenerated on postinstall, prestart, and prebuild. Gitignored.

export const BUILD_INFO = {
  version: ${JSON.stringify(version)},
  buildNumber: ${JSON.stringify(buildNumber)},
  sha: ${JSON.stringify(sha)},
  branch: ${JSON.stringify(branch)},
  dirty: ${dirty},
  builtAt: ${JSON.stringify(builtAt)},
} as const;

export type BuildInfo = typeof BUILD_INFO;
`;

fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
fs.writeFileSync(OUTPUT_PATH, contents, 'utf8');
console.log(`[write-build-info] v0.9.${buildNumber} (${sha}${dirty ? '*' : ''}) [${branch}]`);
